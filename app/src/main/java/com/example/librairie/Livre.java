public class Livre {

    int id;
    String titre;
    String auteur;
    int date;
    String genre;





    public Livre(int id, String titre, String auteur, int date, String genre) {
        this.id = id;
        this.titre = titre;
        this.auteur = auteur;
        this.date = date;
        this.genre = genre;
    }





}
